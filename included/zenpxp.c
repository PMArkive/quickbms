            break;

        case 1:
            ret = zenpxp_1_decompress(in, out, outsz);
            break;

        case 2:
            ret = zenpxp_2_decompress(in, out, outsz);
            break;

        case 3:
        case 4:
            ret = zenpxp_3_4_decompress(in, out, outsz);
